package com.DecisionMakingStatements.Examples;

public class Switch {

	public static void main(String[] args) {
		int n=2;
		
		switch(n) {
		case 0:
			System.out.println("number is 0");
			break;
		case 1:
			System.out.println("number is 1");
			break;
		default:
			System.out.println("number is 2");
		}

	}

}
